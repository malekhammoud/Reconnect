    boolean checkCollision(Player p, Rectangle r) {
        if (p.getTop().intersects(r)) {
            this.vy[1] = 0;
            this.y -= this.v;
            return true;
        }
        if (p.getBottom().intersects(r)) {
            this.vy[0] = 0;
            this.y += this.v;
            return true;
        }
        if (p.getLeft().intersects(r)) {
            this.vx[1] = -0;
            this.x -= this.v;
            return true;
        }
        if (p.getRight().intersects(r)) {
            this.vx[0] = 0;
            this.x += this.v;
            return true;
        }
        return false;
    }
