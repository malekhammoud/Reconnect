    static int x_val = 0;
    static int y_val = 0;
    static int map_v  =1;
    static boolean collision = false;
    void drawmap(Graphics g, int[][] maps){
        int size = 10;
        for (int y = 0; y < maps.length; y++){
            for (int x = 0; x < maps[y].length; x++){
                if (maps[y][x] == 0){
                    g.setColor(Color.BLACK);
                    Rectangle tempRect = new Rectangle(x*size+x_val, y*size+y_val, size, size);
                    if(player.checkCollision(tempRect)){
                        collision = true;
                    }
                    g.fillRect(x*size+x_val, y*size+y_val, size,size);
                };
            }
        }

    }
